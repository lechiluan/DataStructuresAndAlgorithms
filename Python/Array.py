# Array implementation in python
def create_array():
    array = []
    return array

def append(item):
    self.array.append(item)
    return self.array

def pop(self):
    return self.array.pop()

def remove(item):
    self.array.remove(item)
    return self.array

def clear(self):
    self.array = []
    return self.array

def len(self):
    return len(self.array)

